package com.qingshiling.constant;

/**
 * Activity status enumeration class.<br>
 * There is three status for activity: Unpublished, Published and Deleted.<br>
 * <em>Unpublished:</em> Unpublished activity will not be displayed.<br>
 * <em>Published:</em> Published activity is actived.<br>
 * <em>Deleted:</em> Deleted activity will move to the trash.
 * 
 * @author rainisic
 * @version 1.0.0
 * @since 1.0.0
 */
public enum ActivityStatus {
	UNPUBLISHED, PUBLISHED, DELETED
}
